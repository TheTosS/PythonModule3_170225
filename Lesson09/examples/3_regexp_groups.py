import re

text = "Сегодняшняя дата: 2023-10-27"
# Паттерн с тремя захватывающими группами:
# (\d{4}) - Группа 1: Год (4 цифры)
# (\d{2}) - Группа 2: Месяц (2 цифры)
# (\d{2}) - Группа 3: День (2 цифры)
pattern = r'(\d{4})-(\d{2})-(\d{2})'

match = re.search(pattern, text)

if match:
    # match.group(0) или match.group() - все совпадение
    full_match = match.group(0)
    # Доступ к отдельным группам по номеру
    year = match.group(1)
    month = match.group(2)
    day = match.group(3)

    # Можно получить все группы как кортеж
    all_groups = match.groups()

    print(f"Исходный текст: '{text}'")
    print(f"Полное совпадение: {full_match}")
    print(f"Год: {year}")
    print(f"Месяц: {month}")
    print(f"День: {day}")
    print(f"Все группы (кортеж): {all_groups}")
else:
    print("Дата не найдена.")

# Ожидаемый вывод:
# Исходный текст: 'Сегодняшняя дата: 2023-10-27'
# Полное совпадение: 2023-10-27
# Год: 2023
# Месяц: 10
# День: 27
# Все группы (кортеж): ('2023', '10', '27')